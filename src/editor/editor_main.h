/*
 * Main editor class
 */

#ifndef __EDITOR_MAIN_H__
#define __EDITOR_MAIN_H__

typedef class editor
  : public editor_reference
{
  /*
   * Editor parts
   */
  GENERATOR     gen;
  GUI_INTERFACE gui;
  GUI_STATE     state;
  
public:

  GENERATOR       * generator_get(void)
  {
    return(&gen);
  }
  GUI_INTERFACE   * gui_interface_get(void)
  {
    return(&gui);
  }
  GUI_STATE       * gui_state_get(void)
  {
    return(&state);
  }

public:

  void process_help(void);
  bool process_params(int argc, char **argv);
  void configure(void);
  void run(int argc, char **argv);
  
public:

  editor(void);
  ~editor(void);
  
} EDITOR;

#endif // __EDITOR_MAIN_H__
